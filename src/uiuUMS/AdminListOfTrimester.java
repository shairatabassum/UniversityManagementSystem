    private void addItem(){
        try{
            String sql = "select * from TrimesterList";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            while (res.next())
            {
                String add1 = res.getString(1);
                String add2 = res.getString(2);
                String add3 = res.getString(3);
                String TrimesterName = add1 + "  -  " + add2 + " " + add3;
                jComboBox1.addItem(TrimesterName);
            }
            res.close();
            pst.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
