    private void table(String viewTrimester){
        try{
            String sql = "select CourseID, CourseTitle, Faculty from FacultyAllTrimesterHistory where Batch='"+viewTrimester+"'";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(res));
            res.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {
                
            }
        }
    }
    
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            String d1 = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
            String d2 = ((JTextField) jDateChooser2.getDateEditor().getUiComponent()).getText();
            String d3 = ((JTextField) jDateChooser3.getDateEditor().getUiComponent()).getText();
            String d4 = ((JTextField) jDateChooser4.getDateEditor().getUiComponent()).getText();
            if(d1.equals("") || d2.equals("") || d3.equals("") || d4.equals(""))
                JOptionPane.showMessageDialog(null, "Invalid Input", null, JOptionPane.ERROR_MESSAGE);
            else{
                String batch = txtBatch1.getText();
                String add1 = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
                String add2 = ((JTextField) jDateChooser2.getDateEditor().getUiComponent()).getText();
                String add3 = ((JTextField) jDateChooser3.getDateEditor().getUiComponent()).getText();
                String add4 = ((JTextField) jDateChooser4.getDateEditor().getUiComponent()).getText();
                String sql = "update TrimesterList set StartDate='"+add1+"',EndDate='"+add2+"',RegistrationDate='"+add3+"',WithdrawDate='"+add4+"'where Batch='"+batch+"'";
                pst = cnn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Updated!");
                pst.close();
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        try{
            int index = jComboBox1.getSelectedIndex();
            String sql = "select * from TrimesterList where rowid='"+index+"'";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            if(res.next()){
                viewTrimester = res.getString(1);
            }
            res.close();
            pst.close();
            
            String sql2 = "select * from TrimesterList where Batch='"+viewTrimester+"' ";
            pst = cnn.prepareStatement(sql2);
            res = pst.executeQuery();
            if(res.next()){
                String add1 = res.getString(1);
                String add2 = res.getString(2);
                String add3 = res.getString(3);
                String add4 = res.getString(4);
                String add5 = res.getString(5);
                String add6 = res.getString(6);
                String add7 = res.getString(7);
                txtBatch1.setText(add1);
                txtTrimester.setText(add2);
                txtYear.setText(add3);
                
                java.util.Date date = new SimpleDateFormat("MMM d,yyyy").parse(add4);
                jDateChooser1.setDate(date);
                java.util.Date date2 = new SimpleDateFormat("MMM d,yyyy").parse(add5);
                jDateChooser2.setDate(date2);
                java.util.Date date3 = new SimpleDateFormat("MMM d,yyyy").parse(add6);
                jDateChooser3.setDate(date3);
                java.util.Date date4 = new SimpleDateFormat("MMM d,yyyy").parse(add7);
                jDateChooser4.setDate(date4);
            }
            pst.close();
            res.close();
            table(viewTrimester);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {
                
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

