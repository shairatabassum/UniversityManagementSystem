    private void btnUpdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate1ActionPerformed
        // TODO add your handling code here:
        try{
            if(jComboBox1.getSelectedIndex() == 0 || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jComboBox2.getSelectedIndex() == 0 || jComboBox4.getSelectedIndex() == 0)
                    JOptionPane.showMessageDialog(null, "Invalid Input.", null, JOptionPane.ERROR_MESSAGE);
            else{
                String add1 = jComboBox1.getSelectedItem().toString();
                String add2 = jTextField2.getText();
                String add3 = jTextField3.getText();
                String add4 = jComboBox2.getSelectedItem().toString();
                String add5 = jComboBox3.getSelectedItem().toString();
                String add8 = jComboBox4.getSelectedItem().toString();

                String sql = "update CourseList set Department='"+add1+"',CourseID='"+add2+"',CourseTitle='"+add3+"',Credit='"+add4+"',Prerequisite='"+add5+"',OfferedTrimester='"+add8+"'where CourseID='"+add2+"'";
                pst = cnn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Updated!");
                pst.close();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {

            }
        }
        table();
    }//GEN-LAST:event_btnUpdate1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try{
            int row = jTable1.getSelectedRow();
            String table = (jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "select * from CourseList where CourseID = '"+table+"'";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            if(res.next()){
                String add1 = res.getString("Department");
                jComboBox1.setSelectedItem(add1);
                String add2 = res.getString("CourseID");
                jTextField2.setText(add2);
                String add3 = res.getString("CourseTitle");
                jTextField3.setText(add3);
                String add4 = res.getString("Credit");
                jComboBox2.setSelectedItem(add4);
                String add5 = res.getString("Prerequisite");
                jComboBox3.setSelectedItem(add5);
                String add8 = res.getString("OfferedTrimester");
                jComboBox4.setSelectedItem(add8);

                res.close();
                pst.close();
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {

            }
        }
        table();
    }//GEN-LAST:event_jTable1MouseClicked

