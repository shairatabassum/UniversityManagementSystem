    private void addItem(){
        try{
            String sql1 = "select * from CourseList";
            pst = cnn.prepareStatement(sql1);
            res = pst.executeQuery();
            while(res.next()){
                String course = res.getString(2);
                jComboBox3.addItem(course);
            }
            pst.close();
            res.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void table(){
        String sql = "select CourseID, CourseTitle, Credit from CourseList order by CourseID";
        try{
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(res));
            res.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {
                
            }
        }
    }

