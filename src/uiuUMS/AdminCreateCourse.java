    private void btnCreateCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateCourseActionPerformed
        // TODO add your handling code here:
        try{
            if (jComboBox1.getSelectedIndex() == 0 || jTextField3.getText().isEmpty() || jComboBox2.getSelectedIndex() == 0 || jComboBox3.getSelectedIndex() == 0 || jTextField2.getText().isEmpty())
                JOptionPane.showMessageDialog(null, "Invalid Input.", null, JOptionPane.ERROR_MESSAGE);
            else{
                String sql = "Insert into CourseList (Department, CourseID, CourseTitle, Credit, Prerequisite, OfferedTrimester, TotalStudent) values (?,?,?,?,?,?,?)";
                pst = cnn.prepareStatement(sql);
                pst.setString(1, jComboBox1.getSelectedItem().toString());
                pst.setString(2, jTextField2.getText());
                pst.setString(3, jTextField3.getText());
                pst.setString(4, jComboBox2.getSelectedItem().toString());
                pst.setString(5, jComboBox4.getSelectedItem().toString());
                pst.setString(6, jComboBox3.getSelectedItem().toString());
                pst.setString (7, "0");
                pst.execute();
                pst.close();

                int id = 1;
                String sql2 = "select * from ExtraInfo where row='"+id+"'";
                pst = cnn.prepareStatement(sql2);
                res = pst.executeQuery();
                String stringtotalCourse = res.getString(5);
                int totalCourse = Integer.parseInt(stringtotalCourse);
                totalCourse++;
                pst.close();
                res.close();
            
                String sql3 = "update ExtraInfo set TotalCourse='"+totalCourse+"' where row='"+id+"'";
                pst = cnn.prepareStatement(sql3);
                pst.execute();
                pst.close();

                JOptionPane.showMessageDialog(null, "New Course Added!");
                
                
                jComboBox1.setSelectedIndex(0);
                jTextField2.setText("");
                jTextField3.setText("");
                jComboBox2.setSelectedIndex(0);
                jComboBox4.setSelectedIndex(0);
                jComboBox3.setSelectedIndex(0);
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
            }
            catch(Exception e)
            {
                
            }
        }
    }//GEN-LAST:event_btnCreateCourseActionPerformed

