    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try{
            int row = jTable1.getSelectedRow();
            String table = (jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "select * from FacultyList where Name = '"+table+"'";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            if(res.next()){
                String add1 = res.getString("Name");
                txtName.setText(add1);
                String add2 = res.getString("Designation");
                jComboBox1.setSelectedItem(add2);
                String add3 = res.getString("Department");
                jComboBox2.setSelectedItem(add3);
                String add4 = res.getString("Mobile");
                txtMobile.setText(add4);
                String add5 = res.getString("Email");
                txtEmail.setText(add5);
                String add6 = res.getString("Website");
                txtWebsite.setText(add6);
                String add7 = res.getString("Room");
                txtRoom.setText(add7);
                String add8 = res.getString("Username");
                txtUsername.setText(add8);
                
                res.close();
                pst.close();
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {

            }
        }
        table();
    }//GEN-LAST:event_jTable1MouseClicked

