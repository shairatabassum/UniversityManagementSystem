    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            if(txtName.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "No data found to update.", null, JOptionPane.ERROR_MESSAGE);
            }
            else{
                String add1 = txtName.getText();
                String add2 = jComboBox1.getSelectedItem().toString();
                String add3 = jComboBox2.getSelectedItem().toString();
                String add4 = txtMobile.getText();
                String add5 = txtEmail.getText();
                String add6 = txtWebsite.getText();
                String add7 = txtRoom.getText();
                String add8 = txtUsername.getText();
                
                int mobileValidation = MobileValidation(txtMobile.getText());

                //Email Validation
                if(!Pattern.matches("^[0-9a-zA-Z\\.]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[0-9a-zA-Z\\.]+$", txtEmail.getText())){
                JOptionPane.showMessageDialog(null, "Please enter a valid email", "Invalid Email Address", JOptionPane.ERROR_MESSAGE);
                txtEmail.setText("");
                }

                else if(mobileValidation == 0){
                        JOptionPane.showMessageDialog(null, "Please enter a valid number", "Invalid Mobile Number", JOptionPane.ERROR_MESSAGE);
                        txtMobile.setText("");
                    }

                else{
                String sql = "update FacultyList set Name='"+add1+"',Designation='"+add2+"',Department='"+add3+"',Mobile='"+add4+"',Email='"+add5+"',Website='"+add6+"',Room='"+add7+"',Username='"+add8+"'where Name='"+add1+"'";
                pst = cnn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Updated!");
                pst.close();
                    }
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {

            }
        }
        table();
    }//GEN-LAST:event_btnUpdateActionPerformed

