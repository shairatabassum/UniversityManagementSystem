    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try{
            jButton2.setEnabled(true);
            btnAssignFaculty.setEnabled(true);
            int row = jTable1.getSelectedRow();
            String table = (jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "select * from CourseList where CourseID = '"+table+"'";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            if(res.next()){
                String add2 = res.getString("CourseID");
                jLabel3.setText(add2);
                String add3 = res.getString("CourseTitle");
                jLabel4.setText(add3);
                String add4 = res.getString("Credit");
                jLabel5.setText(add4);
                String add6 = res.getString("Faculty");
                res.close();
                pst.close();
                String sql1 = "select * from FacultyList where Username='"+add6+"'";
                pst = cnn.prepareStatement(sql1);
                res = pst.executeQuery();
                if(res.next()){
                    String add7 = res.getString(8);
                    jComboBox1.setSelectedItem(add7);
                    btnAssignFaculty.setEnabled(false);
                }
                else{
                    jComboBox1.setSelectedIndex(0);
                    jButton2.setEnabled(false);
                }
                pst.close();
                res.close();
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {
                
            }
        }
        table();
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnAssignFacultyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignFacultyActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedIndex() == 0)
            JOptionPane.showMessageDialog(null, "Invalid Input.", null, JOptionPane.ERROR_MESSAGE);
        else{
            try{
            String course = jLabel3.getText();
            String student=null;
            String sql4 = "select * from CourseList where CourseID='"+course+"'";
            pst = cnn.prepareStatement(sql4);
            res = pst.executeQuery();
            if(res.next()){
                student = res.getString("TotalStudent");
            }
            pst.close();
            res.close();
            String sql = "Insert into FacultyAllTrimesterHistory (Batch, CourseID, CourseTitle, Credit, Faculty, TotalStudent) values (?,?,?,?,?,?)";
            pst = cnn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jLabel3.getText());
            pst.setString(3, jLabel4.getText());
            pst.setString(4, jLabel5.getText());
            pst.setString(5, (String) jComboBox1.getSelectedItem());
            pst.setString(6, student);

            pst.execute();
            res.close();
            pst.close();

            String add1 = jLabel3.getText();
            String add2 = jTextField1.getText();
            String add3 = (String) jComboBox1.getSelectedItem();
            String sql1 = "update CourseList set Batch='"+add2+"',Faculty='"+add3+"'where CourseID='"+add1+"'";
            pst = cnn.prepareStatement(sql1);
            pst.execute();
            res.close();
            pst.close();
            JOptionPane.showMessageDialog(null, "Faculty assigned.");
            btnAssignFaculty.setEnabled(false);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
            finally{
                try{
                    res.close();
                    pst.close();
                }
                catch(Exception e)
                {

                }
            }
            table();
        }
    }//GEN-LAST:event_btnAssignFacultyActionPerformed

