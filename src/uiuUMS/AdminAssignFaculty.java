    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedIndex() == 0)
            JOptionPane.showMessageDialog(null, "Invalid Input.", null, JOptionPane.ERROR_MESSAGE);
        else{
            String batch = jTextField1.getText();
            String courseID = jLabel3.getText();
            String faculty = (String) jComboBox1.getSelectedItem();
            try{
                String sql = "update FacultyAllTrimesterHistory set Faculty='"+faculty+"' where CourseID='"+courseID+"' and Batch='"+batch+"'";
                pst = cnn.prepareStatement(sql);
                pst.execute();
                pst.close();
                String sql2 = "update CourseList set Faculty = '"+faculty+"' where CourseID='"+courseID+"'";
                pst = cnn.prepareStatement(sql2);
                pst.execute();
                pst.close();
                JOptionPane.showMessageDialog(null, "Faculty Changed and Assigned successfully.");
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            table();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

