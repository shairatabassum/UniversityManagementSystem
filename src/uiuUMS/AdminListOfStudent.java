    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try{
            int row = jTable1.getSelectedRow();
            String table = (jTable1.getModel().getValueAt(row, 3).toString());
            String sql = "select * from StudentList where Username = '"+table+"'";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            if(res.next()){
                String add1 = res.getString("Name");
                txtName.setText(add1);
                String add2 = res.getString("FatherName");
                txtFatherName.setText(add2);
                String add3 = res.getString("MotherName");
                txtMotherName.setText(add3);
                String add4 = res.getString("Address");
                txtAddress.setText(add4);
                String add5 = res.getString("Mobile");
                txtMobile.setText(add5);
                String add6 = res.getString("Email");
                txtEmail.setText(add6);
                String add7 = res.getString("DOB");
                java.util.Date date = new SimpleDateFormat("MMM d,yyyy").parse(add7);
                jDateChooser1.setDate(date);
                String add8 = res.getString("BloodGroup");
                jComboBox1.setSelectedItem(add8);
                String add9 = res.getString("Department");
                jComboBox2.setSelectedItem(add9);
                String add10 = res.getString("Batch");
                jComboBox3.setSelectedItem(add10);
                String add11 = res.getString("CompletedTrimester");
                jComboBox4.setSelectedItem(add11);
                String add12 = res.getString("CompletedCredit");
                txtCompletedCredit.setText(add12);
                String add13 = res.getString("CGPA");
                txtCGPA.setText(add13);
                 String add14 = res.getString("Username");
                txtUsername.setText(add14);
                
                res.close();
                pst.close();
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {

            }
        }
        table();
    }//GEN-LAST:event_jTable1MouseClicked

