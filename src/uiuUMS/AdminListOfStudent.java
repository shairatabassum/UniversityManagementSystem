    private void addItem(){
        try{
            String sql = "select * from TrimesterList";
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            while(res.next()){
                String batch = res.getString(1);
                jComboBox3.addItem(batch);
            }
            pst.close();
            res.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private int MobileValidation(String number){
        Pattern pattern = Pattern.compile("\\d{11}");
        Matcher matcher = pattern.matcher(number);
        
        if(matcher.matches()){
            return 1;
        }
        else
            return 0;
    }
    
    private void table(){
        String sql = "select Department, Batch, Name, Username from StudentList order by Username";
        try{
            pst = cnn.prepareStatement(sql);
            res = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(res));
            res.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                res.close();
                pst.close();
            }
            catch(Exception e)
            {
                
            }
        }
    }

