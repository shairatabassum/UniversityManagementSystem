    private int checkUser(){
        try{
        String username = txtUsername.getText();
                String sql = "select * from StudentList where Username='"+username+"'";
                pst = cnn.prepareStatement(sql);
                res = pst.executeQuery();
                if(res.next()){
                    JOptionPane.showMessageDialog(null, "Username already existed.", null, JOptionPane.ERROR_MESSAGE);
                    txtUsername.setText("");
                    return 1;
                }
                pst.close();
                res.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        return 0;
    }
    
    private int MobileValidation(String number){
        Pattern pattern = Pattern.compile("\\d{11}");
        Matcher matcher = pattern.matcher(number);
        
        if(matcher.matches()){
            return 1;
        }
        else
            return 0;
    }
    
