    private void btnCreateProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateProfileActionPerformed
        // TODO add your handling code here:
        try{
            int checkExistedUsername = checkUser();
            String d1 = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
            if(txtName.getText().isEmpty() || txtFatherName.getText().isEmpty() || txtMotherName.getText().isEmpty() || txtAddress.getText().isEmpty() || txtMobile.getText().isEmpty() || txtEmail.getText().isEmpty() || d1.equals("") || jComboBox1.getSelectedIndex()==0 || jComboBox2.getSelectedIndex()==0 || jComboBox3.getSelectedIndex()==0 || jComboBox4.getSelectedIndex()==0 || txtCompletedCredit.getText().isEmpty() || txtCGPA.getText().isEmpty() || txtUsername.getText().isEmpty() || txtPassword.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Please fill required fields.", null, JOptionPane.ERROR_MESSAGE);
            }
            else{
                String a1 = txtName.getText();
                String a6 = txtAddress.getText();
                String a4 = txtMobile.getText();
                String a5 = txtEmail.getText();
                String a7 = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
                String a8 = jComboBox1.getSelectedItem().toString();
                String a9 = jComboBox2.getSelectedItem().toString();
                String a10 = jComboBox3.getSelectedItem().toString();
                String a2 = txtUsername.getText();
                String a3 = txtPassword.getText();
                
                int mobileValidation = MobileValidation(txtMobile.getText());
                //Email Validation
                if(!Pattern.matches("^[0-9a-zA-Z\\.]+[@]{1}+[0-9a-zA-Z\\.]+[.]{1}+[0-9a-zA-Z\\.]+$", txtEmail.getText())){
                JOptionPane.showMessageDialog(null, "Please enter a valid email", "Invalid Email Address", JOptionPane.ERROR_MESSAGE);
                txtEmail.setText("");
                }
                else if(mobileValidation == 0){
                    JOptionPane.showMessageDialog(null, "Please enter a valid number", "Invalid Mobile Number", JOptionPane.ERROR_MESSAGE);
                    txtMobile.setText("");
                }
                
                else if(checkExistedUsername == 0){
                pst.close();
                res.close();
                if(checkExistedUsername==0){
                    s.add(new Student(a1, a2, a3, a4, a5, a6, a7, a8, a9, a10));
                    InsertDatabase();
                    JOptionPane.showMessageDialog(null, "New Student Profile Created!");
                    txtName.setText("");
                    txtFatherName.setText("");
                    txtMotherName.setText("");
                    txtAddress.setText("");
                    txtMobile.setText("");
                    txtEmail.setText("");
                    jDateChooser1.setCalendar(null);
                    jComboBox1.setSelectedIndex(0);
                    jComboBox2.setSelectedIndex(0);
                    jComboBox3.setSelectedIndex(0);
                    jComboBox4.setSelectedIndex(0);
                    txtCompletedCredit.setText("");
                    txtCGPA.setText("");
                    txtUsername.setText("");
                    
                    //send sms
                    String name = a1;
                    String num = a4;
                    String username = a2;
                    String password = txtPassword.getText();
                    
                    String[] word = name.split(" ");
                    name = word[(word.length)-1];
                    String number = "+88" + num;
                    SMSclass.sms(name, number, username, password);
                    }
                }
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
            }
            catch(Exception e)
            {
                
            }
        }
    }//GEN-LAST:event_btnCreateProfileActionPerformed

